package arithmetic;

public class ArithmeticOperators {
    public static void main(String[] args) {
        int A= 20;

        int B = 30;
        System.out.println(A + B);
        System.out.println(A - B);
        System.out.println(A * B);
        System.out.println(A / B);
        System.out.println(A % B);

    }
}
